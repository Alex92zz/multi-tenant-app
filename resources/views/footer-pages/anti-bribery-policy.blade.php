                        <h4 id="introduction">Introduction</h4>
                        <p>This anti-bribery policy exists to set out the responsibilities of FMV Group Ltd and those who work for us regarding observing and upholding our zero-tolerance position on bribery and corruption.</p>
                        <p>It also exists to act as a source of information and guidance for those working for FMV. It helps them recognise and deal with bribery and corruption issues, as well as understand their responsibilities.
                        </p>
                        <h4 id="policy-statement">Policy Statement</h4>
                        <p>FMV is committed to conducting business in an ethical and honest manner and is committed to implementing and enforcing systems that ensure bribery is prevented. FMV has zero-tolerance for bribery and corrupt activities. We are committed to acting professionally, fairly, and with integrity in all business dealings and relationships, wherever in the world we operate.
                        </p>
                        <p>FMV will constantly uphold all laws relating to anti-bribery and corruption in all the jurisdictions in which we operate. We are bound by the laws of the UK, including the Bribery Act 2010, in regard to our conduct both at home and abroad.
                        </p>

                        <p>FMV recognises that bribery and corruption are punishable by up to ten years of imprisonment and a fine. If our company is discovered to have taken part in corrupt activities, we may be subjected to an unlimited fine, be excluded from tendering for public contracts, and face serious damage to our reputation. It is with this in mind that we commit to preventing bribery and corruption in our business and take our legal responsibilities seriously.
                        </p>

                        <h4 id="covered-by-policy">Who is covered by the Policy?</h4>
                        <p>This anti-bribery policy applies to all employees (whether temporary, fixed term, or permanent), consultants, contractors, trainees, seconded staff, home workers, casual workers, agency staff, volunteers, interns, agents, sponsors, or any other person or persons associated with us (including third parties), or any of our subsidiaries or their employees, no matter where they are located (within or outside of the UK). The policy also applies to Officers, Trustees, Board, and/or Committee members at any level.
                        </p>
                        <p>In the context of this policy, third-party refers to any individual or organisation our company meets and works with. It refers to actual and potential clients, customers, suppliers, distributors, business contacts, agents, advisers, and government and public bodies – this includes their advisors, representatives and officials, politicians, and public parties.
                        </p>

                        <p>Any arrangements our company makes with a third party is subject to clear contractual terms, including specific provisions that require the third party to comply with minimum standards described in this policy.
                        </p>

                        <h4 id="definition-of-bribery">Definition of Bribery</h4>
                        <p>Bribery refers to the act of offering, giving, promising, asking, agreeing, receiving, accepting, or soliciting something of value or of an advantage so to induce or influence an action or decision.
                        </p>
                        <p>A bribe refers to any inducement, reward, or object/item of value offered to another individual to gain commercial, contractual, regulatory, or personal advantage.</p>
                        <p>Bribery is not limited to the act of offering a bribe. If an individual is on the receiving end of a bribe and they accept it, they are also breaking the law.
                        </p>
                        <p>Bribery is illegal. Employees must not engage in any form of bribery, whether it be directly, passively (as described above), or through a third party (such as an agent or distributor). They must not bribe a foreign public official anywhere in the world. They must not accept bribes in any degree and if they are uncertain about whether something is a bribe or a gift or act of hospitality, they must seek further advice from the company’s directors.
                        </p>
                        <h4 id="acceptable-and-not-acceptable">What is and What is NOT acceptable</h4>
                        <p>This section of the policy refers to 4 areas:</p>
                        <ul>
                            <li>Gifts and hospitality</li>
                            <li>Facilitation payments</li>
                            <li>Political contributions</li>
                            <li>Charitable contribution</li>
                        </ul>


                        <h4 id="gifts-hospitality">Gifts and Hospitality</h4>
                        <p>FMV accepts normal and appropriate gestures of hospitality and goodwill (whether given to or received from third parties) so long as the giving or receiving of gifts meets the following requirements:</p>
                        <ul>
                            <li>It is not made with the intention of influencing the party to whom it is being given, to obtain or reward the retention of a business or a business advantage, or as an explicit or implicit exchange for favours or benefits.</li>
                            <li>It is not made with the suggestion that a return favour is expected.</li>
                            <li>It is compliant with local law.</li>
                            <li>It is given in the name of the company, not in an individual’s name.</li>
                            <li>It does not include cash or a cash equivalent (e.g., a voucher or gift certificate).</li>
                            <li>It is appropriate for the circumstances (e.g., giving small gifts around Christmas or as a small thank you to a company for helping with a large project upon completion).</li>
                            <li>It is of an appropriate type and value and given at an appropriate time, considering the reason for the gift.</li>
                            <li>It is given / received openly, not secretly.</li>
                            <li>It is not selectively given to a key, influential person, clearly with the intention of directly influencing them.</li>
                            <li>It is not above a certain excessive value, as pre-determined by the company’s directors (usually in excess of £100).</li>
                            <li>It is not offered to, or accepted from, a government official or representative or politician or political party, without the prior approval of the company’s directors.</li>
                        </ul>
                        <p>Where it is inappropriate to decline the offer of a gift (i.e., when meeting with an individual of a certain religion / culture who may take offence), the gift may be accepted so long as it is declared to the directors, who will assess the circumstances.</p>
                        <p>FMV recognises that the practice of giving and receiving business gifts varies between countries, regions, cultures, and religions, so definitions of what is acceptable and not acceptable will inevitably differ for each.</p>
                        <p>As good practice, gifts given and received should always be disclosed to the directors. Gifts from suppliers should always be disclosed.</p>
                        <p>The intention behind a gift being given / received should always be considered. If there is any uncertainty, the advice of the directors should be sought.</p>




                        <h4 id="facilitation-payments-kickbacks">Facilitation Payments and Kickbacks</h4>
                        <p>FMV does not accept and will not make any form of facilitation payments of any nature. We recognise that facilitation payments are a form of bribery that involves expediting or facilitating the performance of a public official for a routine governmental action. We recognise that they tend to be made by low level officials with the intention of securing or speeding up the performance of a certain duty or action.</p>
                        <p>FMV does not allow kickbacks to be made or accepted. We recognise that kickbacks are typically made in exchange for a business favour or advantage.</p>
                        <p>FMV recognises that, despite our strict policy on facilitation payments and kickbacks, employees may face a situation where avoiding a facilitation payment or kickback may put them or their family’s personal security at risk. Under these circumstances, the following steps must be taken:</p>
                        <ul>
                            <li>Keep any amount to the minimum.</li>
                            <li>Ask for a receipt, detailing the amount and reason for the payment.</li>
                            <li>Create a record concerning the payment.</li>
                            <li>Report this incident to your line manager.</li>
                        </ul>

                        <h4 id="political-contributions">Political Contributions</h4>
                        <p>FMV will not make donations, whether in cash, kind, or by any other means, to support any political parties or candidates. We recognise this may be perceived as an attempt to gain an improper business advantage.</p>

                        <h4 id="charitable-contributions">Charitable Contributions</h4>
                        <p>FMV accepts (and indeed encourages) the act of donating to charities – whether through services, knowledge, time, or direct financial contributions (cash or otherwise) – and agrees to disclose all charitable contributions it makes.</p>
                        <p>Employees must be careful to ensure that charitable contributions are not used to facilitate and conceal acts of bribery.</p>
                        <p>We will ensure that all charitable donations made are legal and ethical under local laws and practices, and that donations are not offered / made without the approval of the directors.</p>




                        <h4 id="employee-responsibilities">Employee Responsibilities</h4>
                        <p>As an employee of Flowlee, you must ensure that you read, understand, and comply with the information contained within this policy, and with any training or other anti-bribery and corruption information you are given.</p>
                        <p>All employees and those under our control are equally responsible for the prevention, detection, and reporting of bribery and other forms of corruption. They are required to avoid any activities that could lead to, or imply, a breach of this anti-bribery policy.</p>
                        <p>If you have reason to believe or suspect that an instance of bribery or corruption has occurred or will occur in the future that breaches this policy, you must notify the directors.</p>
                        <p>If any employee breaches this policy, they will face disciplinary action and could face dismissal for gross misconduct. Flowlee has the right to terminate a contractual relationship with an employee if they breach this anti-bribery policy.</p>




                        <h4 id="training-and-communication">Raising a Concern</h4>
                        <p>This section of the policy covers 3 areas:</p>
                        <ul>
                            <li>How to raise a concern</li>
                            <li>What to do if you are a victim of bribery or corruption</li>
                            <li>Protection</li>
                        </ul>

                        <h4 id="raising-concern">How to Raise a Concern</h4>
                        <p>If you suspect that there is an instance of bribery or corrupt activities occurring in relation to Flowlee, you are encouraged to raise your concerns at as early a stage as possible. If you’re uncertain about whether a certain action or behaviour can be considered bribery or corruption, you should speak to your line manager or the directors.</p>
                        <p>FMV will familiarise all employees with its whistleblowing procedures so employees can vocalise their concerns swiftly and confidently.</p>

                        <h4 id="what-to-do-if-victim">What to do if you are a Victim of Bribery or Corruption</h4>
                        <p>You must tell your directors as soon as possible if you are offered a bribe by anyone, if you are asked to make one, if you suspect that you may be bribed or asked to make a bribe in the near future, or if you have reason to believe that you are a victim of another corrupt activity.</p>


                        <h4 id="protection">Protection</h4>
                        <p>If you refuse to accept or offer a bribe or you report a concern relating to potential act(s) of bribery or corruption, Flowlee understands that you may feel worried about potential repercussions. Flowlee will support anyone who raises concerns in good faith under this policy, even if investigation finds that they were mistaken.</p>
                        <p>FMV will ensure that no one suffers any detrimental treatment as a result of refusing to accept or offer a bribe or other corrupt activities or because they reported a concern relating to potential act(s) of bribery or corruption.</p>
                        <p>Detrimental treatment refers to dismissal, disciplinary action, threats, or unfavorable treatment in relation to the concern the individual raised.</p>
                        <p>If you have reason to believe you’ve been subjected to unjust treatment as a result of a concern or refusal to accept a bribe, you should inform your line manager or the directors immediately.</p>

                        <h4 id="training-communication">Training and Communication</h4>
                        <p>FMV will provide training on this policy as part of the induction process for all new employees. Employees will also receive regular, relevant training on how to adhere to this policy, and will be asked annually to formally accept that they will comply with this policy.</p>
                        <p>FMV’s anti-bribery and corruption policy and zero-tolerance attitude will be clearly communicated to all suppliers, contractors, business partners, and any third parties at the outset of business relations, and as appropriate thereafter.</p>
                        <p>FMV will provide relevant anti-bribery and corruption training to employees etc. where we feel their knowledge of how to comply with the Bribery Act needs to be enhanced. As good practice, all businesses should provide their employees with anti-bribery training where there is a potential risk of facing bribery or corruption during work activities.</p>



                        <h4 id="record-keeping">Record Keeping</h4>
                        <p>FMV will keep detailed and accurate financial records and will have appropriate internal controls in place to act as evidence for all payments made. We will declare and keep a written record of the amount and reason for hospitality or gifts accepted and given and understand that gifts and acts of hospitality are subject to managerial review.</p>

                        <h4 id="monitoring-and-reviewing">Monitoring and Reviewing</h4>
                        <p>FMV directors are responsible for monitoring the effectiveness of this policy and will review the implementation of it on a regular basis. They will assess its suitability, adequacy, and effectiveness. Internal control systems and procedures designed to prevent bribery and corruption are subject to regular audits to ensure that they are effective in practice.</p>
                        <p>Any need for improvements will be applied as soon as possible. Employees are encouraged to offer their feedback on this policy if they have any suggestions for how it may be improved. Feedback of this nature should be addressed to the directors.</p>

                        <p>This policy does not form part of an employee’s contract of employment and FMV may amend it at any time so to improve its effectiveness at combating bribery and corruption.</p>

